#left shift operator
i=10
j=i<<1
print(j)

#right shift operator
i=10
j=i>>1
print(j)

#bitwise and operator(&)

i=10
j=15
k=i&j
print(k)
print(bin(i))
print(bin(j))
print(bin(k))

#bitwise or operator(|)

i=10
j=15
k=i|j
print(k)
print(bin(i))
print(bin(j))
print(bin(k))

#bitwise xor operator(^)

i=10
j=15
k=i^j
print(k)
